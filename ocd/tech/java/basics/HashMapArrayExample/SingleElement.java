import java.util.ArrayList;
import java.util.List;


public class SingleElement {
  private int one;
  private int two;
  
  public SingleElement( int one, int two ) {
    this.one = one;
    this.two = two;
  }

  public int getOne() {
    return one;
  }
  public void setOne(int id) {
    this.one = id;
  }

  public int getTwo() {
    return two;
  }
  public void setTwo(int id) {
    this.two = id;
  }  
}
